const add = (a, b) => {
  return a + b;
};

const sub = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

// % ==> modulus
const modulus = (a, b) => {
  return a % b;
};

module.exports = {
  add,
  sub,
  divide,
  multiply,
  modulus,
};
